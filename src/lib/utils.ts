import { Metadata } from "next";
import { twMerge } from "tailwind-merge";
import { type ClassValue, clsx } from "clsx";

export function cn(...inputs: ClassValue[]) {
  return twMerge(clsx(inputs));
}

export function constructMetadata({
  title = "Shane Le",
  description = "Full Stack Developer Shane Le crafts modern, scalable web solutions with expertise in JavaScript, React, and Node.js. View my featured works and technical skills.",
  image = "/image.jpg",
  icons = "/favicon.ico",
}: {
  title?: string;
  description?: string;
  image?: string;
  icons?: string;
} = {}): Metadata {
  return {
    title,
    description,
    openGraph: {
      title,
      description,
      images: [{ url: image }],
    },
    icons,
    metadataBase: new URL("https://portfolio-v3-shanedle.vercel.app/"),
  };
}
